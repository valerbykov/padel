// Жеребьёвка Американо как ВИСТ-ТУРНИР: за (n-1) раундов каждый игрок напарничает
// с каждым ровно 1 раз И противостоит каждому ровно 2 раза (для 4/8/12 игроков —
// выверенные схемы ниже). Игроки случайно перемешиваются на старте — отсюда «рандом».
// Для прочих размеров, кратных 4, — fallback на круговой метод (только партнёрство).

export function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

